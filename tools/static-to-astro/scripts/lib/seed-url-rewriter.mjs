import fs from "node:fs";
import path from "node:path";
import { buildPublicUrl } from "./storage-upload-planner.mjs";

/**
 * @param {Date} [date]
 */
export function formatBackupTimestamp(date = new Date()) {
  const pad = (n) => String(n).padStart(2, "0");
  return (
    `${date.getFullYear()}${pad(date.getMonth() + 1)}${pad(date.getDate())}-` +
    `${pad(date.getHours())}${pad(date.getMinutes())}${pad(date.getSeconds())}`
  );
}

/**
 * @param {object} params
 */
export function rewriteSeedImageUrls({
  seedDir,
  rewritePlanPath,
  publicBaseUrl,
  apply = false,
}) {
  const seedAbs = path.resolve(seedDir);
  const planAbs = path.resolve(rewritePlanPath);
  /** @type {Record<string, Array<object>>} */
  const plan = JSON.parse(fs.readFileSync(planAbs, "utf8"));

  /** @type {Array<object>} */
  const plannedChanges = [];
  /** @type {Array<object>} */
  const appliedChanges = [];
  /** @type {Array<object>} */
  const skipped = [];
  /** @type {Array<string>} */
  const backups = [];
  /** @type {Array<string>} */
  const targetFiles = [];

  for (const [fileName, rewrites] of Object.entries(plan)) {
    const filePath = path.join(seedAbs, fileName);
    targetFiles.push(fileName);

    if (!fs.existsSync(filePath)) {
      for (const rw of rewrites) {
        skipped.push({
          file: fileName,
          legacy_id: rw.legacy_id,
          field: rw.field,
          reason: "seed file not found",
        });
      }
      continue;
    }

    /** @type {Array<object>} */
    const rows = JSON.parse(fs.readFileSync(filePath, "utf8"));
    let fileChangeCount = 0;

    for (const rw of rewrites) {
      const toUrl = buildPublicUrl(publicBaseUrl, rw.to_storage_path);
      const row = rows.find((r) => (r.legacy_id ?? r.id) === rw.legacy_id);

      if (!row) {
        skipped.push({
          file: fileName,
          legacy_id: rw.legacy_id,
          field: rw.field,
          from: rw.from,
          to: toUrl,
          reason: "legacy_id not found in seed row",
        });
        continue;
      }

      const current = row[rw.field];
      if (current === toUrl) {
        skipped.push({
          file: fileName,
          legacy_id: rw.legacy_id,
          field: rw.field,
          from: rw.from,
          to: toUrl,
          reason: "already set to expected public URL",
        });
        continue;
      }

      if (current !== rw.from) {
        skipped.push({
          file: fileName,
          legacy_id: rw.legacy_id,
          field: rw.field,
          from: rw.from,
          to: toUrl,
          current,
          reason: "current value does not match rewrite plan `from`",
        });
        continue;
      }

      const change = {
        file: fileName,
        legacy_id: rw.legacy_id,
        field: rw.field,
        from: rw.from,
        to: toUrl,
        to_storage_path: rw.to_storage_path,
      };
      plannedChanges.push(change);

      if (apply) {
        row[rw.field] = toUrl;
        appliedChanges.push(change);
        fileChangeCount += 1;
      }
    }

    if (apply && fileChangeCount > 0) {
      const ts = formatBackupTimestamp();
      const backupPath = `${filePath}.bak-${ts}`;
      fs.copyFileSync(filePath, backupPath);
      backups.push(backupPath);
      fs.writeFileSync(filePath, `${JSON.stringify(rows, null, 2)}\n`, "utf8");
    }
  }

  return {
    mode: apply ? "apply" : "dry-run",
    seedDir: seedAbs,
    rewritePlanPath: planAbs,
    publicBaseUrl,
    targetFiles,
    plannedChanges,
    appliedChanges,
    skipped,
    backups,
    candidateCount: plannedChanges.length,
    appliedCount: appliedChanges.length,
  };
}

/**
 * @param {ReturnType<typeof rewriteSeedImageUrls>} result
 */
export function formatSeedRewriteReport(result) {
  const changes = result.mode === "apply" ? result.appliedChanges : result.plannedChanges;

  return [
    "# Seed URL Rewrite Report",
    "",
    "Generated by static-to-astro (Phase 3-G).",
    "",
    "> **Production Supabase Storage has not been uploaded yet.**",
    "> Public URLs below are **planned** from `--public-base-url` + `to_storage_path`.",
    "",
    "## Metadata",
    "",
    `- **Mode:** \`${result.mode}\`${result.mode === "dry-run" ? " (default — seed JSON not modified)" : " (`--apply`)"}`,
    `- **Seed directory:** \`${result.seedDir}\``,
    `- **Rewrite plan:** \`${result.rewritePlanPath}\``,
    `- **Public base URL:** \`${result.publicBaseUrl}\``,
    `- **Generated at:** ${new Date().toISOString()}`,
    "",
    "## Summary",
    "",
    "| Metric | Count |",
    "| --- | ---: |",
    `| Target seed files | ${result.targetFiles.length} |`,
    `| Change candidates | ${result.candidateCount} |`,
    `| Applied changes | ${result.appliedCount} |`,
    `| Skipped / failed | ${result.skipped.length} |`,
    `| Backups created | ${result.backups.length} |`,
    "",
    "## Target seed files",
    "",
    ...result.targetFiles.map((f) => `- \`${f}\``),
    "",
    "## Planned / applied rewrites",
    "",
    changes.length
      ? changes
          .map(
            (c) =>
              `### \`${c.file}\` — \`${c.legacy_id}\` / \`${c.field}\`\n\n` +
              `- **from:** ${c.from}\n` +
              `- **to:** ${c.to}\n` +
              `- **storage path:** \`${c.to_storage_path}\``,
          )
          .join("\n\n")
      : "_No rewrites matched._",
    "",
    result.skipped.length ? "## Skipped or unmatched items" : "",
    result.skipped.length
      ? result.skipped
          .map(
            (s) =>
              `- \`${s.file ?? "—"}\` \`${s.legacy_id}\` \`${s.field}\`: ${s.reason}` +
              (s.current ? ` (current: ${String(s.current).slice(0, 80)}…)` : ""),
          )
          .join("\n")
      : "",
    result.skipped.length ? "" : "",
    "## Backups",
    "",
    result.backups.length
      ? result.backups.map((b) => `- \`${b}\``).join("\n")
      : result.mode === "apply"
        ? "_No backups — no rows changed._"
        : "_None (dry-run)._",
    "",
    "## Production caution",
    "",
    "- Do **not** treat rewritten URLs as live until files exist in Supabase Storage",
    "- Re-run with `--apply` only after verifying `storage-url-rewrite-plan.json`",
    "- Astro `src/data` and production DB are **not** updated by this script",
    "",
  ].join("\n");
}

export function appendPhase3GHomeImageToSupabaseSeedReport(reportPath, summary) {
  const reportAbs = path.resolve(reportPath);
  if (!fs.existsSync(reportAbs)) return;

  const block = [
    "",
    "## Phase 3-G: `home_image_url` in schedules seed",
    "",
    "Phase 3-G maps Astro `schedules.json` field **`home_image`** → Supabase seed **`home_image_url`**.",
    "",
    "### Policy",
    "",
    "- Only populated when the source schedule row has a non-null `home_image` (home card thumbnail)",
    "- Stored alongside `show_on_home` / `home_order` for CMS import",
    "- `schema-draft.sql` adds `home_image_url text` on `schedules`",
    "- URL rewrite plan includes `seed-schedules.json` rows when Wix URLs are present",
    "",
    "### Current counts",
    "",
    `- **Schedules with \`home_image_url\`:** ${summary.homeImageUrlCount ?? "—"}`,
    `- **Rewrite plan entries (schedules):** ${summary.scheduleRewriteCount ?? "—"}`,
    "",
    "> Production DB not modified. Regenerate seed after Astro data changes.",
    "",
  ].join("\n");

  let content = fs.readFileSync(reportAbs, "utf8");
  const marker = "## Phase 3-G: `home_image_url` in schedules seed";
  if (content.includes(marker)) {
    content = `${content.split(marker)[0].trimEnd()}${block}`;
  } else {
    content = `${content.trimEnd()}${block}`;
  }
  fs.writeFileSync(reportAbs, content, "utf8");
}
