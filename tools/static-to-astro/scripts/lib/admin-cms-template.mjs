/**
 * Admin CMS template application (Phase 3-S).
 * Copies templates/admin-cms into generated Astro output when --with-admin-cms is set.
 */

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

export const ADMIN_CMS_TEMPLATE_REL = "templates/admin-cms";
export const ADMIN_CMS_NODE_ADAPTER_VERSION = "^9.1.0";
export const ADMIN_CMS_SUPABASE_VERSION = "^2.107.0";

/**
 * @param {string} [toolRoot]
 */
export function resolveAdminCmsTemplateRoot(toolRoot) {
  const base =
    toolRoot ??
    path.resolve(path.dirname(fileURLToPath(import.meta.url)), "../..");
  return path.join(base, ADMIN_CMS_TEMPLATE_REL);
}

/**
 * @param {string} dir
 * @param {(relPath: string, absPath: string) => void} onFile
 */
function walkFiles(dir, onFile, prefix = "") {
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const rel = prefix ? `${prefix}/${entry.name}` : entry.name;
    const abs = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      walkFiles(abs, onFile, rel);
      continue;
    }
    onFile(rel.replace(/\\/g, "/"), abs);
  }
}

/**
 * @param {string} templateRoot
 */
export function listAdminCmsTemplateFiles(templateRoot) {
  /** @type {Array<{ templateRel: string, outputRel: string, absPath: string }>} */
  const files = [];

  const srcRoot = path.join(templateRoot, "src");
  if (fs.existsSync(srcRoot)) {
    walkFiles(srcRoot, (rel, abs) => {
      files.push({
        templateRel: path.posix.join("src", rel),
        outputRel: path.posix.join("src", rel),
        absPath: abs,
      });
    });
  }

  const envExample = path.join(templateRoot, ".env.example");
  if (fs.existsSync(envExample)) {
    files.push({
      templateRel: ".env.example",
      outputRel: ".env.example",
      absPath: envExample,
    });
  }

  return files.sort((a, b) => a.outputRel.localeCompare(b.outputRel));
}

/**
 * @param {string} outDir
 * @param {string} templateRoot
 */
export function applyAdminCmsTemplate(outDir, templateRoot) {
  const files = listAdminCmsTemplateFiles(templateRoot);
  const copied = [];

  for (const file of files) {
    const dest = path.join(outDir, file.outputRel);
    fs.mkdirSync(path.dirname(dest), { recursive: true });
    fs.copyFileSync(file.absPath, dest);
    copied.push(file.outputRel);
  }

  return copied;
}

/**
 * @param {string | null} baseUrl
 */
export function generatePackageJsonWithAdminCms(baseUrl) {
  const dependencies = {
    astro: "^5.7.0",
    "@astrojs/node": ADMIN_CMS_NODE_ADAPTER_VERSION,
    "@supabase/supabase-js": ADMIN_CMS_SUPABASE_VERSION,
  };

  if (baseUrl) {
    dependencies["@astrojs/sitemap"] = "^3.7.0";
  }

  return JSON.stringify(
    {
      name: "generated-static-astro",
      private: true,
      type: "module",
      scripts: {
        dev: "astro dev",
        build: "astro build",
        preview: "astro preview",
      },
      dependencies,
      devDependencies: {},
    },
    null,
    2,
  );
}

/**
 * @param {string | null} baseUrl
 * @param {string} trailingSlash
 */
export function generateAstroConfigWithAdminCms(baseUrl, trailingSlash = "always") {
  const siteLine = baseUrl ? `  site: "${baseUrl}",\n` : "";

  if (baseUrl) {
    return `import { defineConfig } from "astro/config";
import node from "@astrojs/node";
import sitemap from "@astrojs/sitemap";

// Generated by static-to-astro (Phase 2-F + Phase 3-S Admin CMS)
export default defineConfig({
  adapter: node({ mode: "standalone" }),
${siteLine}  trailingSlash: "${trailingSlash}",
  integrations: [sitemap()],
});
`;
  }

  return `import { defineConfig } from "astro/config";
import node from "@astrojs/node";

// Generated by static-to-astro (Phase 2-F + Phase 3-S Admin CMS)
export default defineConfig({
  adapter: node({ mode: "standalone" }),
  trailingSlash: "${trailingSlash}",
});
`;
}

/**
 * @param {string} outDir
 */
export function detectAdminCmsArtifacts(outDir) {
  const checks = [
    { label: "admin index page", path: "src/pages/admin/index.astro" },
    { label: "admin me API", path: "src/pages/api/admin/me.json.ts" },
    { label: "schedule update API", path: "src/pages/api/admin/schedules/update.json.ts" },
    { label: "admin-auth lib", path: "src/lib/admin-auth.ts" },
  ];

  return checks.map((check) => ({
    ...check,
    exists: fs.existsSync(path.join(outDir, check.path)),
  }));
}

/**
 * @param {object} summary
 */
export function formatAdminCmsTemplateSection(summary) {
  if (!summary?.applied) {
    return [
      "## Admin CMS template",
      "",
      "- **Applied:** no (`--with-admin-cms` not used)",
      "",
    ].join("\n");
  }

  const lines = [
    "## Admin CMS template (Phase 3-S)",
    "",
    `- **Applied:** yes`,
    `- **Template source:** \`${summary.templateSource}\``,
    `- **Copied files:** ${summary.copiedFilesCount}`,
    `- **@astrojs/node adapter added:** ${summary.nodeAdapterAdded ? "yes" : "no"}`,
    `- **@supabase/supabase-js added:** ${summary.supabaseClientAdded ? "yes" : "no"}`,
    `- **Output mode:** ${summary.outputMode}`,
    "",
    "### Known hosting limitation",
    "",
    "- Static FTP hosting **cannot** run `/api/admin/*` (no Node server on shared FTP).",
    "- Public site static deploy and Admin/API on a **separate host** may be required.",
    "- Workflow: Admin edit → \`export-supabase-json.mjs\` → \`npm run build\` → upload \`dist/client/\` only.",
    "",
    "### Copied paths (sample)",
    "",
  ];

  for (const rel of summary.copiedFilesSample ?? []) {
    lines.push(`- \`${rel}\``);
  }
  if ((summary.copiedFilesCount ?? 0) > (summary.copiedFilesSample?.length ?? 0)) {
    lines.push(`- _… and ${summary.copiedFilesCount - summary.copiedFilesSample.length} more_`);
  }
  lines.push("");

  return lines.join("\n");
}

/**
 * @param {{ fixtureLabel: string, summary: object, reportPath: string }} opts
 */
export function writeAdminCmsTemplateReport({ fixtureLabel, summary, reportPath }) {
  const lines = [
    "# Admin CMS Template Report (Phase 3-S)",
    "",
    `- **Fixture:** \`${fixtureLabel}\``,
    `- **Applied:** ${summary.applied ? "yes" : "no"}`,
    `- **Template source:** \`${summary.templateSource}\``,
    `- **Copied files:** ${summary.copiedFilesCount ?? 0}`,
    `- **@astrojs/node adapter added:** ${summary.nodeAdapterAdded ? "yes" : "no"}`,
    `- **Output mode:** ${summary.outputMode ?? "—"}`,
    "",
    "## Hosting limitation",
    "",
    "- Static FTP hosting cannot run server-side `/api/admin/*`.",
    "- Consider Admin on a separate Node host; deploy only `dist/client/` to FTP.",
    "",
  ];

  if (summary.copiedFiles?.length) {
    lines.push("## Copied files", "");
    for (const rel of summary.copiedFiles) {
      lines.push(`- \`${rel}\``);
    }
    lines.push("");
  }

  lines.push("---", `Report: \`${reportPath}\``, "");

  fs.mkdirSync(path.dirname(reportPath), { recursive: true });
  fs.writeFileSync(reportPath, lines.join("\n"), "utf8");
}

/**
 * @param {string} outDir
 * @param {{ templateRoot: string, fixtureLabel: string, toolRoot: string }} opts
 */
export function applyAdminCmsTemplateBundle(outDir, { templateRoot, fixtureLabel, toolRoot }) {
  if (!fs.existsSync(templateRoot)) {
    throw new Error(`Admin CMS template not found: ${templateRoot}`);
  }

  const copiedFiles = applyAdminCmsTemplate(outDir, templateRoot);
  const summary = {
    applied: true,
    templateSource: path.relative(toolRoot, templateRoot) || ADMIN_CMS_TEMPLATE_REL,
    copiedFiles,
    copiedFilesCount: copiedFiles.length,
    copiedFilesSample: copiedFiles.slice(0, 12),
    nodeAdapterAdded: true,
    supabaseClientAdded: true,
    outputMode: "hybrid (prerendered pages + server API routes via @astrojs/node)",
  };

  const reportPath = path.join(
    toolRoot,
    "output",
    "admin-cms-template",
    fixtureLabel === "gosaki-static-site" ? "gosaki" : fixtureLabel,
    "ADMIN_CMS_TEMPLATE_REPORT.md",
  );
  writeAdminCmsTemplateReport({
    fixtureLabel,
    summary,
    reportPath: path.relative(toolRoot, reportPath),
  });

  return summary;
}
