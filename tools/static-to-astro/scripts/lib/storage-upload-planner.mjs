import fs from "node:fs";
import path from "node:path";

const MIGRATION_TYPES = new Set(["wix_url", "external_url"]);

/**
 * @param {string} publicBaseUrl
 * @param {string} storagePath
 */
export function buildPublicUrl(publicBaseUrl, storagePath) {
  const base = publicBaseUrl.replace(/\/+$/, "");
  const rel = storagePath.replace(/^\/+/, "");
  return `${base}/${rel}`;
}

/**
 * @param {string} manifestPath
 * @param {string} bucket
 * @param {string} publicBaseUrl
 */
export function planStorageUpload({ manifestPath, bucket, publicBaseUrl }) {
  const manifestAbs = path.resolve(manifestPath);
  const manifestDir = path.dirname(manifestAbs);
  /** @type {Array<object>} */
  const entries = JSON.parse(fs.readFileSync(manifestAbs, "utf8"));

  const candidates = entries.filter(
    (e) => e.source_url && MIGRATION_TYPES.has(e.source_type) && e.target_path,
  );

  /** @type {Map<string, object>} */
  const byTargetPath = new Map();
  /** @type {Array<object>} */
  const duplicateSkipped = [];

  for (const entry of candidates) {
    const key = entry.target_path;
    if (byTargetPath.has(key)) {
      duplicateSkipped.push({
        target_path: key,
        legacy_id: entry.legacy_id,
        source_file: entry.source_file,
        source_field: entry.source_field,
        kept: byTargetPath.get(key).legacy_id,
      });
      continue;
    }
    byTargetPath.set(key, entry);
  }

  const noTargetPath = entries.filter(
    (e) => e.source_url && MIGRATION_TYPES.has(e.source_type) && !e.target_path,
  );

  const uploadTargets = [...byTargetPath.values()].map((entry) => {
    const resolvedLocal =
      entry.local_file != null ? path.join(manifestDir, entry.local_file) : null;
    const localExists = resolvedLocal != null && fs.existsSync(resolvedLocal);
    return {
      legacy_id: entry.legacy_id,
      source_file: entry.source_file,
      source_field: entry.source_field,
      source_type: entry.source_type,
      source_url: entry.source_url,
      target_path: entry.target_path,
      local_file: entry.local_file,
      resolved_local_file: resolvedLocal,
      local_file_exists: localExists,
      expected_public_url: buildPublicUrl(publicBaseUrl, entry.target_path),
      exclude_reason: null,
    };
  });

  const excluded = [
    ...noTargetPath.map((e) => ({
      legacy_id: e.legacy_id,
      source_file: e.source_file,
      source_field: e.source_field,
      reason: "missing target_path",
    })),
    ...entries
      .filter((e) => e.source_type === "local_path")
      .map((e) => ({
        legacy_id: e.legacy_id,
        source_file: e.source_file,
        source_field: e.source_field,
        reason: "local_path — already on disk, not a remote upload",
      })),
  ];

  const withLocal = uploadTargets.filter((t) => t.local_file_exists).length;
  const withoutLocal = uploadTargets.length - withLocal;

  return {
    manifestPath: manifestAbs,
    manifestDir,
    bucket,
    publicBaseUrl,
    candidateCount: candidates.length,
    uploadTargetCount: uploadTargets.length,
    localFileExistsCount: withLocal,
    localFileMissingCount: withoutLocal,
    duplicateSkipped,
    uploadTargets,
    excluded,
  };
}

/**
 * @param {ReturnType<typeof planStorageUpload>} plan
 */
export function formatStorageUploadPlanReport(plan) {
  const lines = [
    "# Storage Upload Plan",
    "",
    "Generated by static-to-astro (Phase 3-G).",
    "",
    "> **Dry-run only — no files were uploaded to production Supabase Storage.**",
    "",
    "## Metadata",
    "",
    `- **Manifest input:** \`${plan.manifestPath}\``,
    `- **Bucket:** \`${plan.bucket}\``,
    `- **Public base URL:** \`${plan.publicBaseUrl}\``,
    `- **Generated at:** ${new Date().toISOString()}`,
    "",
    "## Summary",
    "",
    "| Metric | Count |",
    "| --- | ---: |",
    `| Upload candidates (before dedupe) | ${plan.candidateCount} |`,
    `| Unique upload targets (after dedupe by \`target_path\`) | ${plan.uploadTargetCount} |`,
    `| \`local_file\` exists on disk | ${plan.localFileExistsCount} |`,
    `| \`local_file\` missing or null | ${plan.localFileMissingCount} |`,
    `| Duplicate paths skipped | ${plan.duplicateSkipped.length} |`,
    "",
    "## Upload targets",
    "",
    "| legacy_id | field | target_path | local_file | local exists | expected_public_url |",
    "| --- | --- | --- | --- | --- | --- |",
    ...plan.uploadTargets.map(
      (t) =>
        `| ${t.legacy_id ?? "—"} | ${t.source_field} | \`${t.target_path}\` | ${t.local_file ? `\`${t.local_file}\`` : "—"} | ${t.local_file_exists ? "yes" : "no"} | \`${t.expected_public_url}\` |`,
    ),
    "",
  ];

  if (plan.duplicateSkipped.length) {
    lines.push("## Deduplicated paths (not uploaded separately)", "");
    for (const d of plan.duplicateSkipped) {
      lines.push(
        `- \`${d.target_path}\`: skipped \`${d.legacy_id}\` (${d.source_file} / ${d.source_field}); kept \`${d.kept}\``,
      );
    }
    lines.push("");
  }

  if (plan.excluded.length) {
    lines.push("## Excluded from upload plan", "");
    for (const x of plan.excluded) {
      lines.push(`- \`${x.legacy_id ?? "—"}\` ${x.source_file ?? ""} / ${x.source_field ?? ""}: ${x.reason}`);
    }
    lines.push("");
  }

  lines.push(
    "## Production status",
    "",
    "- **Production Supabase Storage:** not uploaded (dry-run plan only)",
    "- **Sariswing production site:** not affected",
    "",
    "## Notes for real upload (Phase 3-H+)",
    "",
    "1. Confirm copyright / licensing for Wix-hosted images before upload",
    "2. Use tooling-only credentials (service role) — never commit keys",
    "3. Upload preserving `target_path` under bucket `" + plan.bucket + "`",
    "4. Verify public URL matches `expected_public_url` pattern",
    "5. Run `rewrite-seed-image-urls.mjs --apply` only after upload succeeds",
    "6. Rows without `local_file` need download or re-run `prepare-storage-assets.mjs --download`",
    "",
  );

  return lines.join("\n");
}

export function appendPhase3GUploadToConversionReport(astroDir, summary) {
  const conversionPath = path.join(path.resolve(astroDir), "CONVERSION_REPORT.md");
  if (!fs.existsSync(conversionPath)) return;

  let existingRewriteMode = null;
  let existingSeedModified = null;
  let existingBackups = null;
  let rewriteReportApplied = false;

  const seedRewriteReportPath = summary.seedRewriteRel
    ? path.resolve(path.resolve(astroDir), summary.seedRewriteRel)
    : null;
  if (seedRewriteReportPath && fs.existsSync(seedRewriteReportPath)) {
    const rewriteReport = fs.readFileSync(seedRewriteReportPath, "utf8");
    const modeLine = rewriteReport.match(/\*\*Mode:\*\* `(apply|dry-run)/);
    const appliedLine = rewriteReport.match(/\| Applied changes \| (\d+) \|/);
    const backupsLine = rewriteReport.match(/\| Backups created \| (\d+) \|/);
    if (modeLine?.[1] === "apply") {
      existingRewriteMode = "apply";
      rewriteReportApplied = true;
      const applied = Number(appliedLine?.[1] ?? 0);
      if (applied > 0) existingSeedModified = "yes (`--apply`)";
      if (backupsLine) existingBackups = backupsLine[1];
    }
  }

  const contentBefore = fs.readFileSync(conversionPath, "utf8");
  const marker = "## Storage upload plan & seed URL rewrite (Phase 3-G)";
  if (!rewriteReportApplied && contentBefore.includes(marker)) {
    const section = contentBefore.split(marker)[1]?.split("\n## ")[0] ?? "";
    const modeMatch = section.match(/\*\*Seed rewrite mode:\*\* (\S+)/);
    const modMatch = section.match(/\*\*Seed JSON modified:\*\* (.+)/);
    const bakMatch = section.match(/\*\*Backups created:\*\* (\d+)/);
    if (modeMatch) existingRewriteMode = modeMatch[1];
    if (modMatch) existingSeedModified = modMatch[1];
    if (bakMatch) existingBackups = bakMatch[1];
  }

  const rewriteMode = summary.rewriteMode ?? existingRewriteMode ?? "dry-run";
  const seedModified =
    summary.seedModified != null
      ? summary.seedModified
        ? "yes (`--apply`)"
        : "no (dry-run default)"
      : (existingSeedModified ?? "no (dry-run default)");
  const backupsCreated =
    summary.backupsCreated != null ? summary.backupsCreated : (existingBackups ?? 0);

  const block = [
    "",
    "## Storage upload plan & seed URL rewrite (Phase 3-G)",
    "",
    `- **Upload plan:** \`${summary.uploadPlanRel}\``,
    `- **Seed rewrite report:** \`${summary.seedRewriteRel ?? "_(dry-run pending)_"}\``,
    `- **Upload targets (deduped):** ${summary.uploadTargetCount ?? "—"}`,
    `- **Local files ready:** ${summary.localFileExistsCount ?? "—"} / ${summary.uploadTargetCount ?? "—"}`,
    `- **Seed rewrite mode:** ${rewriteMode}`,
    `- **Seed JSON modified:** ${seedModified}`,
    `- **Backups created:** ${backupsCreated}`,
    `- **\`home_image_url\`:** included in \`seed-schedules.json\` draft (from Astro \`home_image\`)`,
    `- **Production Storage upload:** not performed`,
    `- **Production Sariswing site:** not affected`,
    "",
    "### Phase 3-H (planned)",
    "",
    "- Actual Supabase Storage upload script (tooling env only)",
    "- Apply rewrite to Astro `src/data` after Storage URLs confirmed",
    "- RLS, Auth, admin UI",
    "",
  ].join("\n");

  let content = contentBefore;
  const legacyMarker = "### Phase 3-G (planned)";
  if (content.includes(marker)) {
    content = `${content.split(marker)[0].trimEnd()}${block}`;
  } else if (content.includes("## Storage assets (Phase 3-F)")) {
    const parts = content.split("## Storage assets (Phase 3-F)");
    const afterF = parts[1] ?? "";
    const trimmedAfter = afterF.replace(/\n### Phase 3-G \(planned\)[\s\S]*$/, "").trimEnd();
    content = `${parts[0].trimEnd()}\n## Storage assets (Phase 3-F)${trimmedAfter}${block}`;
  } else {
    content = `${content.trimEnd()}${block}`;
  }
  if (content.includes(legacyMarker) && !content.includes(marker)) {
    content = content.replace(/\n### Phase 3-G \(planned\)[\s\S]*?(?=\n## |\n$|$)/, "");
  }
  fs.writeFileSync(conversionPath, content, "utf8");
}
